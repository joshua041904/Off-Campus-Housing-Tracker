#!/usr/bin/env node
/**
 * Generate Vitest + supertest smoke tests from services/<svc>/openapi.yaml.
 * Only services with a known app factory are supported (see APP_SETUP).
 *
 * Usage:
 *   node scripts/testing/generate-route-smoke-tests.mjs --service=api-gateway
 *   node scripts/testing/generate-route-smoke-tests.mjs --branch-variants   # + empty-body POST + wrong-method GET
 *   node scripts/testing/generate-route-smoke-tests.mjs   # all supported services
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";
import { dirname } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO = join(__dirname, "..", "..");
const MANIFEST = join(REPO, "infra", "services-manifest.json");

/** @type {Record<string, { mode: "app" | "factory"; from: string; name: string }>} */
const APP_SETUP = {
  "api-gateway": { mode: "app", from: "../../src/server.js", name: "app" },
  "auth-service": { mode: "app", from: "../../src/server.js", name: "app" },
  "listings-service": { mode: "factory", from: "../../src/http-server.js", name: "createListingsHttpApp" },
  "booking-service": { mode: "factory", from: "../../src/http-app.js", name: "createBookingHttpApp" },
  "analytics-service": { mode: "factory", from: "../../src/http-server.js", name: "createAnalyticsHttpApp" },
  "trust-service": { mode: "factory", from: "../../src/http-server.js", name: "createTrustHttpApp" },
  "notification-service": { mode: "factory", from: "../../src/http-server.js", name: "createNotificationHttpApp" },
};

function argvService() {
  const a = process.argv.find((x) => x.startsWith("--service="));
  return a ? a.slice("--service=".length).trim() : "";
}

function argvBranchVariants() {
  return process.argv.includes("--branch-variants");
}

function loadManifestServices() {
  const j = JSON.parse(readFileSync(MANIFEST, "utf8"));
  return Array.isArray(j.services) ? j.services : [];
}

function parsePaths(yaml) {
  const lines = yaml.split(/\n/);
  const out = [];
  let inPaths = false;
  for (const line of lines) {
    if (/^paths:\s*$/.test(line)) {
      inPaths = true;
      continue;
    }
    if (!inPaths) continue;
    const t = line.trim();
    if (t === "" || t.startsWith("#")) continue;
    if (!line.startsWith(" ") && !line.startsWith("\t") && t.endsWith(":") && !t.startsWith("/") && !/^paths:/.test(t)) {
      break;
    }
    let pathMatch = t.match(/^"(\/[^"]+)":\s*$/);
    if (!pathMatch) pathMatch = t.match(/^(\/[^:\s]+):\s*$/);
    if (pathMatch) {
      out.push({ path: pathMatch[1], methods: [] });
      continue;
    }
    const meth = t.match(/^(get|post|put|delete|patch|options):\s*$/i);
    if (meth && out.length) out[out.length - 1].methods.push(meth[1].toLowerCase());
  }
  return out.filter((p) => p.methods.length);
}

function minimalBody(path, method) {
  const p = path.toLowerCase();
  const m = method.toLowerCase();
  if (m === "get" || m === "options") return null;
  if (m === "delete") return null;
  if (p.includes("/register")) return JSON.stringify({ email: "a@a.com", password: "testpass123" });
  if (p.includes("/login")) return JSON.stringify({ email: "a@a.com", password: "testpass123" });
  if (p.includes("/refresh")) return JSON.stringify({ refreshToken: "dummy" });
  if (p.includes("listing-feel") || p.includes("insights")) return JSON.stringify({ listingId: "00000000-0000-4000-8000-000000000001" });
  return JSON.stringify({});
}

function generateServiceFile(service, branchVariants) {
  const setup = APP_SETUP[service];
  if (!setup) {
    console.error(`generate-route-smoke-tests: skip ${service} (no APP_SETUP — add factory or app export)`);
    return true;
  }
  const openapi = join(REPO, "services", service, "openapi.yaml");
  if (!existsSync(openapi)) {
    console.error(`generate-route-smoke-tests: missing ${openapi}`);
    return false;
  }
  const raw = readFileSync(openapi, "utf8");
  const paths = parsePaths(raw);
  const outDir = join(REPO, "services", service, "tests", "auto");
  mkdirSync(outDir, { recursive: true });
  const outFile = join(outDir, "routes.auto.test.ts");

  const useVitestSuiteAgent = service === "api-gateway";
  const b = [];
  b.push(`/* eslint-disable — generated by scripts/testing/generate-route-smoke-tests.mjs */`);
  if (useVitestSuiteAgent) {
    b.push(`import { vitestRouteHitAgent } from "../vitest-route-hit-agent.js";`);
  } else {
    b.push(`import request from "supertest";`);
  }
  b.push(`import { describe, it, expect, beforeAll } from "vitest";`);
  b.push(``);
  b.push(`describe("AUTO OpenAPI route smoke (${service})", () => {`);
  b.push(`  let app: import("express").Express;`);
  if (useVitestSuiteAgent) {
    b.push(`  let agent: ReturnType<typeof vitestRouteHitAgent>;`);
  }
  b.push(`  beforeAll(async () => {`);
  if (setup.mode === "app") {
    b.push(`    const mod = await import(${JSON.stringify(setup.from)});`);
    b.push(`    app = mod.${setup.name};`);
  } else {
    b.push(`    const mod = await import(${JSON.stringify(setup.from)});`);
    b.push(`    app = mod.${setup.name}() as import("express").Express;`);
  }
  if (useVitestSuiteAgent) {
    b.push(`    agent = vitestRouteHitAgent(app);`);
  }
  b.push(`  });`);
  b.push(``);

  const reqBase = useVitestSuiteAgent ? "agent" : "request(app)";
  for (const { path: pathStr, methods } of paths) {
    for (const method of methods) {
      const name = `AUTO ${method.toUpperCase()} ${pathStr}`;
      let req = `${reqBase}.${method}(\`${pathStr.replace(/`/g, "\\`")}\`)`;
      const body = minimalBody(pathStr, method);
      if (body && method !== "get" && method !== "options") {
        req += `.set("Content-Type", "application/json").send(${body})`;
      }
      if (pathStr.includes("/validate") || pathStr.includes("/refresh")) {
        req += `.set("Authorization", "Bearer test")`;
      }
      b.push(`  it(${JSON.stringify(name)}, async () => {`);
      b.push(`    const res = await ${req};`);
      b.push(`    expect(res.status).toBeGreaterThanOrEqual(200);`);
      b.push(`    expect(res.status).toBeLessThan(600);`);
      b.push(`  });`);
      b.push(``);

      if (branchVariants && method !== "get" && method !== "options") {
        const empty = `${reqBase}.${method}(\`${pathStr.replace(/`/g, "\\`")}\`).set("Content-Type", "application/json").send({})`;
        b.push(`  it(${JSON.stringify(`AUTO ${method.toUpperCase()} ${pathStr} empty body`)}, async () => {`);
        b.push(`    const res = await ${empty};`);
        b.push(`    expect(res.status).toBeGreaterThanOrEqual(200);`);
        b.push(`    expect(res.status).toBeLessThan(600);`);
        b.push(`  });`);
        b.push(``);
      }
      if (branchVariants && method === "post") {
        const wrong = `${reqBase}.get(\`${pathStr.replace(/`/g, "\\`")}\`)`;
        b.push(`  it(${JSON.stringify(`AUTO GET ${pathStr} wrong method (was POST in spec)`)}, async () => {`);
        b.push(`    const res = await ${wrong};`);
        b.push(`    expect(res.status).toBeGreaterThanOrEqual(200);`);
        b.push(`    expect(res.status).toBeLessThan(600);`);
        b.push(`  });`);
        b.push(``);
      }
    }
  }
  b.push(`});`);
  b.push(``);
  writeFileSync(outFile, b.join("\n"));
  console.error(`generate-route-smoke-tests: wrote ${outFile} (${paths.length} paths)`);
  return true;
}

function main() {
  const one = argvService();
  const services = one ? [one] : Object.keys(APP_SETUP);
  const branchVariants = argvBranchVariants();
  let ok = true;
  for (const s of services) {
    if (!generateServiceFile(s, branchVariants)) ok = false;
  }
  process.exit(ok ? 0 : 1);
}

main();
